<p align=justify><h2><a href> SEJARAH POP</a></h2></p>
<img src="images/bgpop.jpg" width="500">
<p style="text-indent:15px;">Musik pop (istilah yang awalnya berasal dari singkatan dari "populer") adalah sebuah genre musik dari musik populer yang berasal dalam bentuk modern pada 1950-an, yang berasal dari rock and roll. Istilah musik populer dan musik pop sering digunakan secara bergantian, meskipun yang pertama adalah deskripsi musik yang populer (dan dapat termasuk gaya apapun), sedangkan yang terakhir adalah genre tertentu yang mengandung kualitas daya tarik massa.

Sebagai genre, musik pop sangat eklektik, sering meminjam elemen dari gaya-gaya lain termasuk urban, dance, rock, latin dan country; Musik pop umumnya dianggap sebagai sebuah genre yang komersial dicatat dan keinginan untuk memiliki daya tarik audiens massa.</p><p>

<p style="text-indent:15px;">Musik pop telah menjadi industri menguntungkan di Amerika Seri</DD>kat sejak abad ke-19. Pada akhir 1950-an muncul genre pop/rock sesudah memudarnya era rock and roll. Pop/rock tahap awal dipengaruhi oleh susunan beat dan gaya rock and roll (dan kadang-kadang doo-wop), tapi tidak terlalu keras seperti rock and roll. Menjelang berakhirnya tahun 1960-an, genre pop/rock mulai dipengaruhi psikedelik dan blue-eyed soul. Pada tahun 1970-an, pop/rock makin cenderung lembut, misalnya seperti musik pop yang dibawakan Burt Bacharach. Artis pop/rock terkenal dari tahun 1960-an misalnya The Beach Boys, The Four Seasons, The Everly Brothers, The Association, The Rascals, The Righteous Brothers, The Walker Brothers, dan Petula Clark. Komposer Burt Bacharach, produser Phil Spector, dan tim pencipta lagu dari Brill Building seperti Barry/Greenwich dan Goffin/King termasuk di antara tokoh musik pop/rock tahun 1960-an.</p><p>
