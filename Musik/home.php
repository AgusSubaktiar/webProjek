<p align=justify><h2><a href>Musik</a></h2></p>
<p style="text-indent:15px;">Musik berasal dari kata muse(s) yang merupakan para dewi yang melambangkan seni dalam mitologi Yunani. Mereka dianggap sebagai sumber ilmu pengetahuan dan inspirasi seni.

Pada awalnya, versi Boeotians menyebutkan hanya ada tiga orang Muse dan mereka tinggal di Gunung Helicon, mereka adalah: Aoide (lagu), Melete (meditasi), Mneme (memori). Tetapi menurut versi yang lebih populer mengatakan bahwa mereka ada sembilan. Muses merupakan anak-anak dari Zeus dan Mnemosyne. Muse lahir setelah Zeus mengawini Mnemosyne selama sembilan hari berturut-turut, mereka lahir dan tinggal di sekitar Peiria.

Setiap muse memiliki spesialisasi masing-masing, yaitu: Calliope (puisi kepahlawanan), Klaio (sejarah), Erato (puisi cinta), Euterpe (sajak), Melpomene (tragedi), Polyhymnia (puisi suci), Terpsichore (paduan suara dan tarian), Thaleia (komedi), Urania (astronomi). Mereka biasa terlihat mengelilingi Apollo, dewa musik di Olympus.<br><img src="images/imghome.jpg" width="500" height="125"><br><p align="center"><font size="-2"><em>Lukisan "The Muses Urania and Calliope" 
Karya Pelukis Simon Vouet</em></font>
<p>
Mungkin Calliope adalah yang paling terkenal diantara sembilan muses tersebut. Bukan hanya karena Calliope merupakan kakak sulung perempuan, namun karena dia juga yang melahirkan dua musisi hebat, yaitu Orpheus dan Linus, salah satu dari mereka dinamakan Oeagus anak Pierus oleh Apollo atau raja Thracian.

Perkembangan Musik
Musik telah mengalami perjalanan yang sangat panjang. Pada awalnya musik merupkan media penyembahan kepada dewa-dewa. Namun seiring perkembangan zaman, maka definisi musik juga mengalami perkembangan yang bermacam-macam, tergantung dari sejarah, budaya serta tempat tinggal individu tersebut.Jenis musik yang biasa kita dengar ada 3 :
<ul>
<ol>1. Pop</ol>
<ol>2. Jazz</ol>
<ol>3. Rock</ol>
</ul>
<strong>Definisi Musik</strong><br>
Definisi musik yang berkembang hingga masa sekarang, antara lain:
<ul>
<li>Bunyi atau kesan terhadap sesuatu yang ditangkap oleh indra pendengaran.</li>
<li>Suatu karya seni dengan segenap unsur pokok dan pendukungnya.</li>
<li>Segala bunyi yang dihasilkan secara sengaja oleh seseorang/kumpulan dan disajikan sebagai musik.</li>
<li>Beberapa orang menganggap musik sama sekali tidak berwujud.</li>
</ul>
Definisi musik mungkin saja berbeda antara satu individu dengan individu lainnya. Hal tersebut disebabkan karena perbedaan sudut pandang, budaya, tujuan dan latar belakang yang berbeda-beda.</p>
